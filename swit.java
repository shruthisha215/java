public class swit
{
    public static void main(String args[])
    {
        int choice=2;
        switch(choice)
        {
            case 1:
             System.out.println("coffee");
             break;
             case 2:
             System.out.println("cold coffee");
             break;
             case 3:
             System.out.println("chocolate coffee");
             break;
             case 4:
                System.out.println("hot chocolate");
                break;
                default:
                    System.out.println("invalid");
        }
    }
}