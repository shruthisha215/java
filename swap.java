public class swap
{
    public static void main(String args[])
    {
        int a=5,b=6;
        System.out.println("before swapping: " +a);
        System.out.println(b);
        a=(5-5)+6;
        b=(6-6)+5;
        System.out.println("after swapping:");
        System.out.println("a:" +a);
        System.out.println("b:"+b);
        
    }
}